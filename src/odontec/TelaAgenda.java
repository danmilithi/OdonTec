    public void adicionarConsultaNaTabela(Agenda c) {
        DefaultTableModel model = (DefaultTableModel) tabelaAgenda.getModel();

        model.addRow(new Object[]{
            c.getId(),
            c.getPaciente(),
            c.getDentista(),
            c.getData(),
            c.getHora(),
            c.getTratamento()
        });
    }
    
    public static ArrayList<Agenda> getConsultas() {
        return consultas;
    }

